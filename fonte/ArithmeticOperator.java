package fonte;

public enum ArithmeticOperator {
    ADD, //Adds values on either side of the operator (+)
    SUB, //Subtracts right hand operand from left hand operand (-)
    MULT, //Multiplies values on either side of the operator (*)
    DIV, //Divides left hand operand by right hand operand (/)
    MOD; //Divides left hand operand by right hand operand and returns remainder (%)
}
