public enum AssignmentOperator {
    ASSIGN, //Assigns values from right side operands to left side operand (=)
    ADD_ASSIGN, //Equivalent to c = c + a (+=)
    SUB_ASSIGN, //Equivalent to c = c - a (-=)
    MULT_ASSIGN, //Equivalent to c = c * a (*=)
    DIV_ASSIGN, //Equivalent to c = c / a (/=)
    MOD_ASSIGN; //Equivalent to c = c % a (%=)
}
